import type { Group, Song } from "../Types/Interfaces";
import supabase from "./supaConfig";

export default supabase;

export const registerUser = async (username: string, email: string, password: string) => {

    const { data, error } = await supabase
        .from('users')
        .insert([{ username, email, password }])
        .select();

    if (error) {
        throw error;
    }

    return data;
}

export const loginUser = async (email: string, password: string) => {
    const { data, error } = await supabase
        .from('users')
        .select('*')
        .eq('email', email)
        .eq('password', password)
        .single();

    if (error) {
        throw error;
    }

    return data;
}

export const getAllGroups = async (username: string) => {
    
    const { data, error } = await supabase
        .from('groups')
        .select('*');

    if (error) {
        throw error;
    }

    const userGroups: Group[] = []
    const otherGroups: Group[] = []
    const joinedGroups: Group[] = []

    data.forEach(group => {
        if (group.users[0] === username) {
            userGroups.push(group);
        } else if (group.users.includes(username)) {
            joinedGroups.push(group);
        } else {
            otherGroups.push(group);
        }
    });

    return {
        userGroups,
        otherGroups,
        joinedGroups
    }
}

